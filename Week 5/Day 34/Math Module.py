# Project Title: Math Module

import math

# Calculate square root
sqrt = math.sqrt(16)

# Calculate factorial
factorial = math.factorial(5)

# Calculate power
power = math.pow(2, 3)

# Display results
print("Square root of 16 is:", sqrt)
print("Factorial of 5 is:", factorial)
print("2 raised to the power of 3 is:", power)
